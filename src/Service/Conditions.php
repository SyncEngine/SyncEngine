<?php

namespace SyncEngine\Service;

class Conditions
{
	const OPERATOR_SET              = 'set';
	const OPERATOR_NOT_SET          = 'not_set';
	const OPERATOR_EMPTY            = 'empty';
	const OPERATOR_NOT_EMPTY        = 'not_empty';
	const OPERATOR_IN               = 'in';
	const OPERATOR_NOT_IN           = 'not';
	const OPERATOR_IN_STRICT        = 'in_strict';
	const OPERATOR_NOT_IN_STRICT    = 'not_strict';
	const OPERATOR_HAS_KEY          = 'has_key';
	const OPERATOR_NOT_HAS_KEY      = 'not_has_key';
	const OPERATOR_LESSER           = '<';
	const OPERATOR_GREATER          = '>';
	const OPERATOR_LESSER_OR_EQUAL  = '<=';
	const OPERATOR_GREATER_OR_EQUAL = '>=';
	const OPERATOR_NOT_EQUAL        = '!=';
	const OPERATOR_EQUAL            = '==';
	const OPERATOR_NOT_EQUAL_STRICT = '!==';
	const OPERATOR_EQUAL_STRICT     = '===';

	const OPERATORS = [
		self::OPERATOR_SET,
		self::OPERATOR_NOT_SET,
		self::OPERATOR_EMPTY,
		self::OPERATOR_NOT_EMPTY,
		self::OPERATOR_IN,
		self::OPERATOR_NOT_IN,
		self::OPERATOR_IN_STRICT,
		self::OPERATOR_NOT_IN_STRICT,
		self::OPERATOR_HAS_KEY,
		self::OPERATOR_NOT_HAS_KEY,
		self::OPERATOR_LESSER,
		self::OPERATOR_GREATER,
		self::OPERATOR_LESSER_OR_EQUAL,
		self::OPERATOR_GREATER_OR_EQUAL,
		self::OPERATOR_NOT_EQUAL,
		self::OPERATOR_EQUAL,
		self::OPERATOR_NOT_EQUAL_STRICT,
		self::OPERATOR_EQUAL_STRICT,
	];

	public function validate( array $conditions, mixed $data = null ): bool
	{
		// @todo Parse conditions? Currently done in Execute Service.

		foreach ( $conditions as $condition ) {
			if ( ! $condition ) {
				continue;
			}
			if ( ! $this->_validate( $condition, $data ) ) {
				return false;
			}
		}

		return true;
	}

	protected function _validate( array $condition, mixed $data = null ): bool
	{
		/**
		 * In case the SOURCE is generated by a tag (storage for example).
		 *
		 * @todo Should we add an extra property "data". Or should we adjust the conditional configs?
		 * @link https://github.com/JoryHogeveen/SyncEngine/issues/91
		 */
		if ( ! empty( $condition['source'] ) ) {
			$data = $condition['source'];
		}

		$key = $condition['key'] ?? '';
		if ( ! $key ) {

			if ( empty( $condition['operator'] ) ) {
				return true;
			}

			// Direct value comparison.
			$data = [ 'data' => $data ];
			$key  = 'data';
		} elseif ( is_array( $key ) ) {
			/**
			 * In case the KEY is generated by a tag.
			 */
			$data = [ 'data' => $key ];
			$key  = 'data';
		}

		$data = ResourceData::create( $data );

		$compare  = $condition['compare'] ?? null;
		$operator = $condition['operator'] ?? null;

		if ( ! $operator ) {
			$operator = ( is_array( $compare ) ) ? 'in' : 'default';
		}

		switch ( $operator ) {
			case self::OPERATOR_SET:
				return isset( $data[ $key ] );
			case self::OPERATOR_NOT_SET:
				return ! isset( $data[ $key ] );
			case self::OPERATOR_EMPTY:
				return empty( $data[ $key ] );
			case self::OPERATOR_NOT_EMPTY:
				return ! empty( $data[ $key ] );
			case self::OPERATOR_IN:
				if ( isset( $data[ $key ] ) ) {
					if ( is_array( $data[ $key ] ) ) {
						return in_array( $compare, $data[ $key ] );
					}

					return str_contains( $data[ $key ], $compare );
				}

				return false;

			case self::OPERATOR_NOT_IN:
				if ( isset( $data[ $key ] ) ) {
					if ( is_array( $data[ $key ] ) ) {
						return ! in_array( $compare, $data[ $key ] );
					}

					return ! str_contains( $data[ $key ], $compare );
				}

				return true;

			case self::OPERATOR_IN_STRICT:
				if ( isset( $data[ $key ] ) ) {
					if ( is_array( $data[ $key ] ) ) {
						return in_array( $compare, $data[ $key ], true );
					}

					return str_contains( $data[ $key ], $compare );
				}

				return false;

			case self::OPERATOR_NOT_IN_STRICT:
				if ( isset( $data[ $key ] ) ) {
					if ( is_array( $data[ $key ] ) ) {
						return ! in_array( $compare, $data[ $key ], true );
					}

					return ! str_contains( $data[ $key ], $compare );
				}

				return true;

			case self::OPERATOR_HAS_KEY:
			case 'haskey':
				if ( ! is_string( $compare ) && ! is_numeric( $compare ) ) {
					return false;
				}

				// Check value first.
				$value = $data[ $key ] ?? null;
				if ( ! $value ) {
					return false;
				}

				return isset( $value[ $compare ] );

			case self::OPERATOR_NOT_HAS_KEY:
			case 'nothaskey':
				if ( ! is_string( $compare ) && ! is_numeric( $compare ) ) {
					return true;
				}

				// Check value first.
				$value = $data[ $key ] ?? null;
				if ( ! $value ) {
					// @todo Error?
					return true;
				}

				return ! isset( $value[ $compare ] );

			case self::OPERATOR_LESSER:
				return $compare < $data[ $key ];
			case self::OPERATOR_GREATER:
				return $compare > $data[ $key ];
			case self::OPERATOR_LESSER_OR_EQUAL:
				return $compare <= $data[ $key ];
			case self::OPERATOR_GREATER_OR_EQUAL:
				return $compare >= $data[ $key ];
			case self::OPERATOR_NOT_EQUAL:
				return $compare != $data[ $key ];
			case self::OPERATOR_EQUAL:
				return $compare == $data[ $key ];
			case self::OPERATOR_NOT_EQUAL_STRICT:
				return $compare !== $data[ $key ];
			case self::OPERATOR_EQUAL_STRICT:
			default:
				return $compare === $data[ $key ];
		}
	}
}
